package Clases;

public class Vehiculo_aereo extends Vehiculo{
    private int n_motores, n_pasajeros;
    private String modelo, aerolinea;

    public int getN_motores() {
        return n_motores;
    }

    public void setN_motores(int n_motores) {
        this.n_motores = n_motores;
    }

    public int getN_pasajeros() {
        return n_pasajeros;
    }

    public void setN_pasajeros(int n_pasajeros) {
        this.n_pasajeros = n_pasajeros;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getAerolinea() {
        return aerolinea;
    }

    public void setAerolinea(String aerolinea) {
        this.aerolinea = aerolinea;
    }
    
    
    
}
