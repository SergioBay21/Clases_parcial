package Clases;

public class Helicoptero extends Vehiculo_aereo{
    private int n_helices, n_puertas;
    private String tipo;

    public int getN_helices() {
        return n_helices;
    }

    public void setN_helices(int n_helices) {
        this.n_helices = n_helices;
    }

    public int getN_puertas() {
        return n_puertas;
    }

    public void setN_puertas(int n_puertas) {
        this.n_puertas = n_puertas;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
}
